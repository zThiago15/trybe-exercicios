const a = 10;
const b = 5;

// Soma
function somar(){
    return a + b;
}
console.log(somar(10, 5)); // 15

// Subtração
function subtrair(a, b){
    return a - b;
}
console.log(subtrair(100, 30)); // 70

// Multiplicação
function multiplicar(a, b){
    return a * b;
}
console.log(multiplicar(5, 4)); // 20

function dividir(a, b){
    return a / b;
}

console.log(dividir(90, 3)); // 30

function restoDaDivisao(a, b){
    return a % b;
}
console.log(restoDaDivisao(18, 2)); // 0
